package flowcontrols;

/**
 * using do while loop
 * @author sp22082
 *
 */

public class DoWhileLoopSample {

	public static void main(String[] args) {
		
		System.out.println("print the numbers");
		 int i=1;
		 do
         {
       	  System.out.println(i);
       	  i++;
         } while(i>=10);

		 
		 System.out.println("do while example");
		 int j=100;
		 do
         {
       	  System.out.println(j);
       	  i++;
         } while(j>=10);
		 
	}

}
